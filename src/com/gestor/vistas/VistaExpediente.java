    private void llenarCombos() {

        List<String> listaDelitos = obtenerListaDelitos();
        List<String> listaPersonas = obtenerListaNombresPersonas();
        DefaultComboBoxModel modeloDelitos = new DefaultComboBoxModel(listaDelitos.toArray());
        DefaultComboBoxModel modeloPersonas = new DefaultComboBoxModel(listaPersonas.toArray());
        comboPersona.setModel(modeloPersonas);
        comboDelito.setModel(modeloDelitos);

    }

    private List<String> obtenerListaNombresPersonas() {
        personaService = new PersonaImpl();
        List<Persona> personas = personaService.listar();
        List<String> listaNombres = new ArrayList<String>();
        for (Persona listaPersonas : personas) {
            listaNombres.add(listaPersonas.getNombre() + " " + listaPersonas.getApellido() + " ("+listaPersonas.getDni()+")");
        }
        return listaNombres;
    }

    private List<String> obtenerListaDelitos() {
        delitoService = new DelitoImpl();
        List<Delito> delitos = delitoService.listarDelitos();
        List<String> listaDelitos = new ArrayList<String>();
        for (Delito delito : delitos) {
            listaDelitos.add(delito.getCaratula() + " ("+delito.getCodDelito()+")");
        }
        return listaDelitos;
    }

